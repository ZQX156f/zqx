package com.cskaoyan.basic.operator;

/*
        比较（comparison）运算符指的是用来判断两个变量（或常量）大小的运算符。显然既然是比较，
        自然需要两个操作数，比较运算符是典型的二元运算符。比较的结果，也就是比较运算符组成的表达式
        （也叫布尔表达式）的结果必然是一个布尔值，也就是true或者false。
 */
public class Demo3 {

    public static void main(String[] args) {

        int a = 1;
        int b = 2;
        System.out.println(a == b);
    }
}
