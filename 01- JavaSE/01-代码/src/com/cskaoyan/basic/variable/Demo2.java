package com.cskaoyan.basic.variable;


/*
      注意事项:
        - Java当中的整数字面值常量，默认当作int处理。
        - 假如你想使用字面值整数定义一个long类型变量，那么该字面值常量的后面应该加上一个"L"区分（禁止使用小写"l"）
        - Java当中的小数字面值常量，默认当作double处理。假如你想使用字面值小数定义一个float类型变量，
          那么该字面值常量的后面应该加上一个"F"或"f"区分。
 */
public class Demo2 {

    public static void main(String[] args) {
        // 整数字面值常量默认是int类型
        int a = 10;
        // 显示生命long类型
        long b = 99999999999L;

        // 显示生命float类型
        float floatValue = 0.5f;


    }
}
